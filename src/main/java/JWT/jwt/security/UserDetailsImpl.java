package JWT.jwt.security;

import JWT.jwt.dto.auth.AuthDto;
import JWT.jwt.dto.userDto.GetUserDto;
import JWT.jwt.entities.UserEntity;
import org.springframework.security.core.GrantedAuthority;
import org.springframework.security.core.userdetails.UserDetails;

import java.util.Collection;
import java.util.Collections;

public class UserDetailsImpl implements UserDetails {

    private final AuthDto user;

    public UserDetailsImpl(AuthDto user) {
        this.user = user;
    }

    @Override
    public Collection<? extends GrantedAuthority> getAuthorities() {
        return Collections.emptyList();
    }

    @Override
    public String getPassword() {
        return user.getPasswordDto();
    }

    @Override
    public String getUsername() {
        return user.getEmailDto();
    }

    @Override
    public boolean isAccountNonExpired() {
        return true;
    }

    @Override
    public boolean isAccountNonLocked() {
        return true;
    }

    @Override
    public boolean isCredentialsNonExpired() {
        return true;
    }

    @Override
    public boolean isEnabled() {
        return true;
    }

    public String getNombre(){
        return user.getUserNameDto();
    }
}
